/**
 * parser.js
 * 
 * Why this file exists:
 * Users upload raw CSV spreadsheets which contain text-based numbers and 
 * blank/malformed rows. This file parses the CSV format into raw rows, 
 * and then sanitizes (cleans) the columns to make them ready for mathematics.
 * 
 * How it works:
 * 1. It calls `Papa.parse()` to read the CSV text.
 * 2. It loops through each raw row, stripping empty spaces, converting text 
 *    numbers into real JavaScript Numbers (`parseInt` / `parseFloat`), and 
 *    verifying that the row contains all required columns.
 * 3. Invalid rows are skipped, acting as a real-world data cleaning process.
 */

window.CSVParser = {
  
  /**
   * Parses raw CSV string data into a sanitized array of objects.
   * @param {string} csvText - Raw CSV text string from file or sample data
   * @returns {Array|null} - Array of clean sales objects, or null if parsing fails
   */
  parse: function(csvText) {
    // Check if input is valid text
    if (!csvText || typeof csvText !== "string" || csvText.trim() === "") {
      console.error("CSV Parser: Invalid or empty input string.");
      return null;
    }

    // Call PapaParse. We pass 'header: true' so it uses the first row columns as object keys.
    // 'skipEmptyLines: true' ignores completely empty lines at the end of the file.
    const result = Papa.parse(csvText, {
      header: true,
      skipEmptyLines: true
    });

    // Check for critical parsing errors
    if (result.errors && result.errors.length > 0 && result.data.length === 0) {
      console.error("CSV Parser: Critical parsing errors occurred:", result.errors);
      return null;
    }

    // Clean and sanitize the parsed rows
    return this.cleanData(result.data);
  },

  /**
   * Loops through raw objects and sanitizes column fields.
   * @param {Array} rawRows - Array of objects parsed directly from the CSV
   * @returns {Array} - Cleaned array of objects
   */
  cleanData: function(rawRows) {
    const cleanedRows = [];

    // Loop through every single row generated by the CSV parser
    for (let i = 0; i < rawRows.length; i++) {
      const rawRow = rawRows[i];

      // 1. Data Cleaning: Trim whitespace from keys and values
      const row = {};
      
      // Object.keys retrieves all column header names in this row (e.g. "Date", "Price")
      const keys = Object.keys(rawRow);
      for (let j = 0; j < keys.length; j++) {
        const key = keys[j];
        // Clean the key (header) and the value by trimming trailing/leading spaces
        const cleanKey = key.trim();
        const rawValue = rawRow[key];
        const cleanValue = typeof rawValue === "string" ? rawValue.trim() : rawValue;
        
        row[cleanKey] = cleanValue;
      }

      // 2. Data Validation: Ensure all 9 required columns exist and are not empty
      if (!row.Date || !row.Customer || !row.Product || !row.Category || 
          !row.Region || !row.Quantity || !row.Price || !row.Revenue || !row.Profit) {
        console.warn(`Row ${i + 1} skipped: Missing one or more required columns.`);
        continue; // Skip this row and go to the next iteration
      }

      // 3. Data Conversion: Convert string representations of numbers to real numeric types
      const quantity = parseInt(row.Quantity, 10);
      const price = parseFloat(row.Price);
      const revenue = parseFloat(row.Revenue);
      const profit = parseFloat(row.Profit);

      // Verify that the conversions did not result in NaN (Not a Number)
      if (isNaN(quantity) || isNaN(price) || isNaN(revenue) || isNaN(profit)) {
        console.warn(`Row ${i + 1} skipped: Number conversions returned NaN.`, row);
        continue; // Skip row
      }

      // 4. Date Validation: Support both YYYY-MM-DD and DD/MM/YYYY formats
      let parsedDate = null;
      let dateIsoString = "";

      if (row.Date.includes("/")) {
        // Assume DD/MM/YYYY format
        const parts = row.Date.split("/");
        if (parts.length === 3) {
          const day = parseInt(parts[0], 10);
          const month = parseInt(parts[1], 10) - 1; // Month is 0-indexed in JS Date
          const year = parseInt(parts[2], 10);
          parsedDate = new Date(year, month, day);
          
          if (!isNaN(parsedDate.getTime())) {
            const padMonth = (month + 1) < 10 ? "0" + (month + 1) : (month + 1);
            const padDay = day < 10 ? "0" + day : day;
            dateIsoString = `${year}-${padMonth}-${padDay}`;
          }
        }
      } else if (row.Date.includes("-")) {
        // Assume YYYY-MM-DD format
        const parts = row.Date.split("-");
        if (parts.length === 3) {
          const year = parseInt(parts[0], 10);
          const month = parseInt(parts[1], 10) - 1;
          const day = parseInt(parts[2], 10);
          parsedDate = new Date(year, month, day);
          
          if (!isNaN(parsedDate.getTime())) {
            dateIsoString = row.Date;
          }
        }
      }

      // Check if Date object was successfully created
      if (!parsedDate || isNaN(parsedDate.getTime()) || !dateIsoString) {
        console.warn(`Row ${i + 1} skipped: Invalid date format: ${row.Date}`);
        continue; // Skip row
      }

      // 5. Structure the finalized clean record object
      const cleanRecord = {
        date: dateIsoString,              // Internal standardized YYYY-MM-DD for sorting/filtering
        customer: row.Customer,      // Format: CUST-1001
        product: row.Product,        // e.g. "Laptop Pro"
        category: row.Category,      // e.g. "Electronics"
        region: row.Region,          // e.g. "North"
        quantity: quantity,          // Integer
        price: price,                // Float
        revenue: revenue,            // Float
        profit: profit               // Float
      };

      // Push clean record to the final array
      cleanedRows.push(cleanRecord);
    }

    console.log(`Successfully parsed and cleaned ${cleanedRows.length} out of ${rawRows.length} rows.`);
    return cleanedRows;
  }
};
